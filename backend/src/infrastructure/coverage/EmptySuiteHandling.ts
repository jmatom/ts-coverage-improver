import { mkdir, readdir, stat, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { SupportedTestFramework } from '@domain/ports/TestGeneratorPort';

/**
 * The location our placeholder test file goes when the workdir has no
 * tests. Lives only inside the per-job sandboxed clone — the user's
 * actual repository is never touched. The directory + filename are
 * deliberately distinctive so anyone reading lcov output / test logs
 * understands "this row is from the improver, not the project."
 */
export const PLACEHOLDER_DIR = '__improver-placeholder__';
export const PLACEHOLDER_FILE = 'placeholder.test.ts';

/**
 * `describe`/`it` are auto-globals in jest, so the test body needs no
 * imports. For vitest the safer path is the explicit import — works
 * regardless of whether the project has `globals: true` in its config.
 */
const PLACEHOLDER_BODY: Record<SupportedTestFramework, string> = {
  jest: `// Auto-generated by ts-coverage-improver to allow Jest to exit 0 when
// the target repository has no tests of its own. Lives only inside the
// per-job sandboxed clone — the user's actual repository is not modified.
// Wiped on the next clone.
describe('coverage-improver placeholder', () => {
  it('placeholder', () => {});
});
`,
  vitest: `// Auto-generated by ts-coverage-improver to allow Vitest to exit 0 when
// the target repository has no tests of its own. Lives only inside the
// per-job sandboxed clone — the user's actual repository is not modified.
// Wiped on the next clone.
import { describe, it } from 'vitest';

describe('coverage-improver placeholder', () => {
  it('placeholder', () => {});
});
`,
  // Mocha + nyc/c8 don't fail on empty suites the same way, and there's
  // no equivalent of jest's --collectCoverageFrom in this stack. Empty-
  // suite handling for mocha is out of scope for v1; the runner errors
  // out with a clear message before we reach this branch.
  mocha: '',
};

/**
 * Quick async filesystem walk: does this workdir contain any file that
 * looks like a test file? Pattern matches the conventions every supported
 * framework picks up by default:
 *   - `<name>.test.{ts,tsx,js,jsx}`
 *   - `<name>.spec.{ts,tsx,js,jsx}`
 *   - any path inside a `__tests__/` directory
 *
 * Stops at the first hit so the cost is bounded — typical no-tests repo
 * walks the whole tree once (small repos), typical with-tests repo
 * returns within tens of files. Skips `node_modules`, `.git`, `dist`,
 * `build`, `coverage` since those don't carry user test files.
 */
export async function hasAnyTestFile(workdir: string): Promise<boolean> {
  const SKIP = new Set(['node_modules', '.git', 'dist', 'build', 'coverage', PLACEHOLDER_DIR]);
  const TEST_FILE_PATTERN = /\.(test|spec)\.(ts|tsx|js|jsx)$/;

  async function walk(dir: string): Promise<boolean> {
    let entries;
    try {
      entries = await readdir(dir, { withFileTypes: true });
    } catch {
      return false;
    }
    for (const entry of entries) {
      if (entry.isDirectory()) {
        if (SKIP.has(entry.name)) continue;
        if (entry.name === '__tests__') return true;
        if (await walk(join(dir, entry.name))) return true;
      } else if (entry.isFile() && TEST_FILE_PATTERN.test(entry.name)) {
        return true;
      }
    }
    return false;
  }

  return walk(workdir);
}

/**
 * Writes a per-framework placeholder test into
 * `<workdir>/__improver-placeholder__/placeholder.test.ts`. The location
 * matches the default `testMatch` pattern of jest + vitest
 * (any-depth-glob plus `*.test.ts`), so the placeholder is discovered
 * without any project-side config change.
 *
 * Idempotent: writing twice is a no-op overwrite. Caller doesn't need to
 * clean up — the per-job workdir is disposable and gets wiped on the
 * next clone.
 */
export async function writePlaceholderTest(
  workdir: string,
  framework: SupportedTestFramework,
): Promise<void> {
  const body = PLACEHOLDER_BODY[framework];
  if (!body) return;
  const dir = join(workdir, PLACEHOLDER_DIR);
  await mkdir(dir, { recursive: true });
  await writeFile(join(dir, PLACEHOLDER_FILE), body, 'utf8');
}

/**
 * CLI flags to append to the framework's coverage command when the
 * workdir has no real tests. Two jobs:
 *   1. Make the runner exit 0 even though only the placeholder ran
 *      (`--passWithNoTests` — supported by both jest and vitest).
 *   2. Make the coverage report list every source file at 0%, not just
 *      the (zero) files imported by the placeholder. Without this the
 *      coverage table on the dashboard would render empty and the user
 *      would have nothing to click "Improve" on.
 *
 * The source-file glob is hard-coded to `src/**` for v1 — covers ~95% of
 * TS projects. Reading `tsconfig.json`'s `include`/`exclude` to derive
 * a project-specific glob is a possible follow-up; not implemented here.
 */
export function emptySuiteFlags(framework: SupportedTestFramework): string[] {
  switch (framework) {
    case 'jest':
      return [
        '--passWithNoTests',
        '--collectCoverageFrom=src/**/*.{ts,tsx,js,jsx}',
        '--collectCoverageFrom=!**/*.test.*',
        '--collectCoverageFrom=!**/*.spec.*',
        `--collectCoverageFrom=!**/${PLACEHOLDER_DIR}/**`,
      ];
    case 'vitest':
      return [
        '--passWithNoTests',
        '--coverage.include=src/**/*.{ts,tsx,js,jsx}',
        `--coverage.exclude=**/${PLACEHOLDER_DIR}/**`,
      ];
    case 'mocha':
      return [];
  }
}
